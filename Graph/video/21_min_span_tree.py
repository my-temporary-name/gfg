# Minimum Spanning Tree (MST) / Prim's Algorithm
# In minimum spanning tree, we find the tree that connects all the vertices in the graph and the total weight of the tree is minimum.

# For a graph with V vertices, the minimum spanning tree will have V-1 edges.

# Prism Algorithm is a greedy algorithm that finds the minimum spanning tree for a weighted graph.
# It maintains two sets of vertices:
# 1. The vertices that are included in the minimum spanning tree.
# 2. The vertices that are not included in the minimum spanning tree.
# It starts with an empty set and keeps adding the vertex with the minimum weight edge until all the vertices are included in the minimum spanning tree.
