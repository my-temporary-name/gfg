# Kruskals Algorithm , TC = O(ElogE), SC = O(V)
# Kruskal's algorithm is a minimum spanning tree algorithm that takes a graph as input and finds the subset of the edges of that graph which
# form a tree that includes every vertex has the minimum sum of weights among all the trees that can be formed from the graph.

# Minimum Spanning Tree (MST) is a tree that connects all the vertices of the graph and has the minimum possible total edge weight.
# 1. Sort all edges in increasing order of their weight.
# 2. Initialize: MST=[], result=0
# 3. Do following for each edge while MST size is less than V-1:
#   a. If adding e to MST does not form a cycle, include e in MST.
#      MST = MST + e
#      res = res + e.weight
# 4. Return res

# We will use disjoint set data structure to check if adding an edge will form a cycle or not.

# Kruskal's Algorithm


class Graph:
    def __init__(self, vertices):
        self.V = vertices
        self.graph = []

    def add_edge(self, u, v, w):
        self.graph.append([u, v, w])

    # Search function

    def find(self, parent, i):
        if parent[i] == i:
            return i
        return self.find(parent, parent[i])

    def apply_union(self, parent, rank, x, y):
        xroot = self.find(parent, x)
        yroot = self.find(parent, y)
        if rank[xroot] < rank[yroot]:
            parent[xroot] = yroot
        elif rank[xroot] > rank[yroot]:
            parent[yroot] = xroot
        else:
            parent[yroot] = xroot
            rank[xroot] += 1

    #  Applying Kruskal algorithm
    def kruskal_algo(self):
        result = []
        i, e = 0, 0
        self.graph = sorted(self.graph, key=lambda item: item[2])
        parent = []
        rank = []
        for node in range(self.V):
            parent.append(node)
            rank.append(0)
        while e < self.V - 1:
            u, v, w = self.graph[i]
            i = i + 1
            x = self.find(parent, u)
            y = self.find(parent, v)
            if x != y:
                e = e + 1
                result.append([u, v, w])
                self.apply_union(parent, rank, x, y)
        for u, v, weight in result:
            print("%d - %d: %d" % (u, v, weight))


